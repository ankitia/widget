package com.ia.modal;

public class MasterListBuildingURL 
{
	private long listBuildUrlId;
	private String url;
	private int userId;
	private String status;
	
	
	public long getListBuildUrlId() {
		return listBuildUrlId;
	}
	public void setListBuildUrlId(long listBuildUrlId) {
		this.listBuildUrlId = listBuildUrlId;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
