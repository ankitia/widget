package com.ia.modal;

public class MasterCompanyURL 
{
	private long companyUrlId;
	private String url;
	private int userId;
	private String status;
	public long getCompanyUrlId() {
		return companyUrlId;
	}
	public void setCompanyUrlId(long companyUrlId) {
		this.companyUrlId = companyUrlId;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
